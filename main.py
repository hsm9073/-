import streamlit as st
import pandas as pd

# 1. 웹페이지 기본 설정
st.set_page_config(
    page_title="한국인 영양소 섭취 추이 분석",
    page_icon="🥗",
    layout="centered"
)

# 2. 데이터 구축 (제공해주신 이미지 파일의 데이터를 기반으로 구성)
# 캡처.PNG -> 단백질, 2.PNG -> 칼슘, 3.PNG -> 나트륨
data = {
    "연도": [1998, 2001, 2005, 2007, 2008, 2009, 2010, 2011, 2012, 2013, 2014, 2015, 2016, 2017, 2018, 2019, 2020, 2021, 2022, 2023, 2024],
    "단백질(g)": [76.1, 73.5, 77.7, 68.6, 69.2, 70.7, 77.5, 76.4, 75.3, 74.2, 74.1, 76.7, 76.7, 75.5, 74.8, 75.5, 74.3, 74.0, 72.1, 73.0, 75.7],
    "칼슘(mg)": [503.7, 503.3, 560.5, 477.5, 495.3, 499.8, 534.4, 518.0, 508.3, 500.3, 494.6, 510.1, 526.5, 524.4, 517.3, 492.1, 486.6, 486.3, 488.4, 495.2, 499.0],
    "나트륨(mg)": [4951.2, 5430.2, 5691.8, 4854.9, 5006.5, 5073.3, 5225.6, 5211.0, 4942.1, 4176.3, 4033.2, 4188.7, 3585.5, 3586.5, 3488.4, 3455.6, 3350.4, 3224.1, 3213.3, 3282.3, 3279.6]
}

df = pd.DataFrame(data)
df.set_index("연도", inplace=True)

# 3. 메인 타이틀 및 소개
st.title("🤔 한국인은 짜게 먹는 습관을 고쳤을까?")
st.subheader("국민건강영양조사(19세 이상 표준화) 데이터로 보는 영양소 섭취 추이")

st.markdown("""
한국인의 고질적인 식습관 중 하나로 꼽히는 **'짜게 먹는 습관'**. 
과연 지난 20여 년간 우리 식탁은 얼마나 변했을까요? 
보내주신 원본 데이터 파일(`캡처.PNG`, `2.PNG`, `3.PNG`)을 통합하여 분석해 보았습니다.
""")

st.divider()

# 4. 핵심 질문 및 나트륨 그래프 (스트림릿 기본 그래프 사용)
st.header("1. 나트륨 섭취량 추이 (mg)")
st.markdown("""
* **세계보건기구(WHO) 하루 나트륨 권장량:** **2,000mg 미만**
""")

# 스트림릿 기본 라인 차트 사용
st.line_chart(df["나트륨(mg)"])

# 분석 내용 서술
st.info("""
**📊 데이터 분석 결과:**
* **확연한 감소세:** 2005년 **5,691.8mg**으로 정점을 찍었던 나트륨 섭취량이 2024년에는 **3,279.6mg**까지 크게 감소했습니다. 
* **결론:** 한국인은 **짜게 먹는 습관을 상당 부분 고쳐나가고 있습니다!** 정부의 저감화 정책과 국민들의 건강 인식이 긍정적인 변화를 이끌어낸 것으로 보입니다.
* **하지만 ⚠️:** 여전히 WHO 권장량(2,000mg)에 비하면 **약 1.6배** 높게 섭취하고 있어, 지속적인 관리가 필요합니다.
""")

st.divider()

# 5. 다른 영양소와의 비교 (단백질, 칼슘)
st.header("2. 다른 영양소들은 어떨까?")
st.write("나트륨이 줄어드는 동안, 우리 몸에 꼭 필요한 단백질과 칼슘의 섭취량은 어떻게 변했을까요?")

# 탭 기능으로 깔끔하게 분리
tab1, tab2 = st.tabs(["🥛 칼슘 (mg)", "🥩 단백질 (g)"])

with tab1:
    st.markdown("**칼슘 섭취량 추이** (성인 권장량: 약 700~800mg)")
    st.bar_chart(df["칼슘(mg)"])
    st.warning("📉 **칼슘 부족 심각:** 칼슘은 지난 20년간 500mg 안팎에서 정체되어 있으며, 오히려 최근 들어 소폭 감소했습니다. 권장량에 한참 못 미치는 수준입니다.")

with tab2:
    st.markdown("**단백질 섭취량 추이**")
    st.line_chart(df["단백질(g)"])
    st.success("💪 **단백질은 안정적:** 단백질 섭취량은 경제 성장 및 식생활 서구화와 맞물려 연간 70g대 중반을 꾸준하고 안정적으로 유지하고 있습니다.")

st.divider()

# 6. 원본 데이터 표 보기 기능
st.header("3. 통합 데이터 테이블")
st.write("분석에 사용된 원본 데이터 테이블입니다.")
st.dataframe(df.style.format({
    "단백질(g)": "{:.1f}g",
    "칼슘(mg)": "{:.1f}mg",
    "나트륨(mg)": "{:,.1f}mg"
}))

st.caption("출처: 국민건강영양조사 영양소 섭취량 통계 데이터 기반 (제공 파일: 캡처.PNG, 2.PNG, 3.PNG)")
